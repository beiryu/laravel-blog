    public function update(Request $request, User $user)
    {
        //
        $request->validate([
            'name' => 'required',
            'role' => 'required',
        ]);


        $name = $request->input('name');
        $role = $request->input('role');

        $user->name = $name;
        $user->role = $role;

        $user->save();

        return redirect(route('users.index'))->with('status', 'User Edited Successfully');
